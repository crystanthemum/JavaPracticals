package interfaces;

public class Mountain{

	//instance vars
	private String name;
	private int height;

	//getters and setters for the instance variables

	public void setName(String name){
	this.name = name;
	}

	public String getName(){
	return name;
	}

	public void setHeight(int height){
	this.height = height;
	}

	public int getHeight(){
	return height;
	}

	}

