package oopPractical;
/*
 * This class will output some literary quotes
 * @author Crystal
 */
public class LiteraryQuotations {
	/*
	 * This method will print out a shakespeare quote
	 */
	public void shakespeareQuote() {
		System.out.println("Good night, good night! Parting is such a sweet sorrow, "
				+ "that I shall say good night till it be morrow");
	}
	/*
	 * This method will print out an oscar wilde quote
	 */
	public void oscarWildeQuote() {
		System.out.println("Experience is simply the name we give our mistakes");
	}
	/*
	 * This method will print out a churchill quote
	 */
	public void churchillQuote() {
		System.out.println("Continous effort - not strength or intelligence - is the key to unlocking our potential");
	}

	public void georgeWBushQuote() {
		System.out.println("One of the great things about books is sometimes there are some fantastic pictures");
	}

	public void albertEinsteinQuote() {
		System.out.println("If the facts don't fit the theory, change the facts");
	}

}
