package oops;

public class UnitConverter { //class name
	//creating an object/method in that class
	public double cmToInches(double cm) {
		return (cm*0.394);
	}
	public double inchesTocm (double inches) {
		return (inches*2.54);
	}
	public double kmToMiles (double kms) {
		return (kms * 0.621);
	}
	public double milesToKms (double miles) {
		return (miles *1.609);
	}
}
