package oops;

public class Mountains {

	// instance vars
	private String name;
	private int height;
	
	
	
	public String getName() {
		return name;
	}
	public int getHeight() {
		return height;
	}
	public String setName(String name) {
		return this.name = name;
	}
	public int setHeight(int height) {
		return this.height = height;
	}
}
